/**
 * 周波数帯域/チャンネルを設定する。
 * @param newBand 周波数帯域 UHF/BS/CS
 * @param newChannel チャンネル
 */
void
FriioExpress::setChannel(BandType newBand, int newChannel)
{
  if (BAND_UHF != newBand || newChannel < 13 || newChannel > 62) {
    throw traceable_error("unknown channel."); // TODO: 適当なエラークラス
  }
  
  uint fd = tunerFd;
  int index, i;

  index =  (newChannel- 13) * 11;

  if(chdata[index] != newChannel) {
    throw traceable_error("channel data error."); 
  }

  // ここで書き換え
  usbdata[1887] = chdata[index+1];

  for(i = 0; i < 8; i++) {
    usbdata[1942 + (i*13)] = chdata[index+2];
    usbdata[1943 + (i*13)] = chdata[index+3 + i];
  }
 
  usb_ctrl_sends(fd, usbdata, DATALEN(usbdata), NULL, 0);

}

